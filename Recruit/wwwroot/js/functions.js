function openNav() {
    document.getElementById("mySidenav").style.width = "250px";
    document.getElementById("contantslide").style.marginLeft = "250px";
    document.getElementById("headerslide").style.marginLeft = "250px";
    document.getElementById("footerslide").style.marginLeft = "250px";
}

